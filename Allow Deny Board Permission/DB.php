	global $smcFunc;

	db_extend('packages');

	$table_columns = $smcFunc['db_list_columns']('{db_prefix}boards');

	$deny_column = array(
		'name' => 'member_groups_deny',
		'type' => 'varchar',
		'size' => 255,
		'null' => false,
		'default' => '',
	);

	// Add the column the new column
	return $smcFunc['db_add_column']('{db_prefix}boards', $deny_column, null, 'skip_if_exists');

	updateSettings(array('enable_allow_deny' => '1'));

